package com.mycompany.mymovielist.model;

import jakarta.persistence.*;
/**
 *
 * @author kiran
 */

@MappedSuperclass
public abstract class BaseEntity{
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
}
